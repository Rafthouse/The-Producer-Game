/** Зв'язок жанрів з типовими темами */
const GENRE_THEMES: Record<string, string[]> = {
  punk: ['protest', 'alcohol', 'absurd', 'village'],
  rap: ['money', 'protest', 'glory', 'alcohol'],
  pop: ['love', 'glory', 'absurd', 'money'],
  folk: ['village', 'food', 'nostalgia', 'love'],
  bard: ['nostalgia', 'existential', 'depression', 'love'],
  electro: ['absurd', 'money', 'alcohol', 'glory'],
}

/** Парсить промпт і визначає домінантні теми */
function parsePrompt(prompt: string): string[] {
  const p = prompt.toLowerCase()
  const themes: string[] = []

  // Визначаємо теми за ключовими словами
  const rules: [RegExp, string][] = [
    [/любо|коха|романтик|серце|дівчин|дівчин|красун|весіл|стосунк/, 'love'],
    [/протест|револю|бунт|політик|депутат|влада|проти\s+систем/, 'protest'],
    [/алкоголь|буха|пив|горілк|п'ян|самогон|вечірк|бар/, 'alcohol'],
    [/село|корова|город|сало|курник|бабц|дід|картопл|кабачок/, 'village'],
    [/депрес|сум|самотн|біль|провал|втом|дно|темря/, 'depression'],
    [/ностальг|спомин|минул|дитинств|колись|стар/, 'nostalgia'],
    [/абсурд|сюр|божевіль|дивн|геніально|тупи/, 'absurd'],
    [/грош|прибут|бізнес|мільйон|багат|дорог|люкс/, 'money'],
    [/слав|зірк|хіт|концерт|фан|продюсер|сцен/, 'glory'],
    [/екзистен|сенс|житт|час|всесвіт|філософ|думк/, 'existential'],
    [/їж|борщ|смак|варени|холодець|ковбас|суп|каш/, 'food'],
  ]

  for (const [re, theme] of rules) {
    if (re.test(p)) themes.push(theme)
  }

  // Якщо нічого не знайдено — випадкові 2 теми з улюблених жанру
  return themes.length > 0 ? themes : []
}

/**
 * Генерує текст пісні на основі промпту.
 * Якщо промпт порожній — випадковий набір рядків (стара механіка).
 */
export const generateLyricsFromPrompt = (prompt: string, genreId?: string): string => {
  if (!prompt || prompt.trim().length === 0) {
    return pickMany(LINES, 4).join('\n')
  }

  const themes = parsePrompt(prompt)

  // Якщо промпт не дав тем — використати теми жанру
  let usedThemes = themes
  if (usedThemes.length === 0 && genreId && GENRE_THEMES[genreId]) {
    usedThemes = pickMany(GENRE_THEMES[genreId], randInt(2, 3))
  }

  // Додати трохи абсурду для колориту
  if (usedThemes.length === 0) usedThemes = ['absurd', 'village']

  const result: string[] = []

  // Беремо рядки з визначених тем
  for (const theme of usedThemes) {
    const lines = THEME_LINES[theme]
    if (lines) {
      // 1-2 рядки з теми
      const count = randInt(1, 2)
      const picked = pickMany(lines, Math.min(count, lines.length))
      result.push(...picked)
    }
  }

  // Добиваємо випадковими рядками до 4+
  while (result.length < 4) {
    result.push(pick(LINES))
  }

  // Перемішуємо, щоб не було блоків
  const shuffled = result.sort(() => Math.random() - 0.5)

  // Повертаємо 4-6 рядків
  const final = shuffled.slice(0, randInt(4, 6))
  return final.join('\n')
}

